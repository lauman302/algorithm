'''
Улица, на которой хочет жить Тимофей, имеет длину n, то есть состоит из n
одинаковых идущих подряд участков. На каждом участке либо уже построен дом,
либо участок пустой. Тимофей ищет место для строительства своего дома. Он
очень общителен и не хочет жить далеко от других людей, живущих на этой улице.

Чтобы оптимально выбрать место для строительства, Тимофей хочет для каждого
участка знать расстояние до ближайшего пустого участка.
Для пустого участка эта величина будет равна нулю –— расстояние до самого себя.

Ваша задача –— помочь Тимофею посчитать искомые расстояния.
Для этого у вас есть карта улицы. Дома в городе Тимофея нумеровались
в том порядке, в котором строились, поэтому их номера на карте никак
не упорядочены. Пустые участки обозначены нулями.


Формат ввода:

В первой строке дана длина улицы —– n (1 ≤ n ≤ 106).
В следующей строке записаны n целых неотрицательных чисел — номера домов
и обозначения пустых участков на карте (нули). Гарантируется, что
в последовательности есть хотя бы один ноль. Номера домов (положительные числа)
уникальны и не превосходят 109.

Формат вывода:

Для каждого из участков выведите расстояние до ближайшего нуля.
Числа выводите в одну строку, разделяя их пробелами.
'''


def sleight_hands(panel, max_keys):
    deleted = set()
    scoring = {}
    for num in panel:
        if num not in deleted:
            if num not in scoring:
                scoring[num] = 1
            else:
                scoring[num] += 1
                if scoring[num] > max_keys:
                    deleted.add(num)
                    del scoring[num]
    return len(scoring)


if __name__ == '__main__':
    max_keys = int(input()) * 2
    panel = [int(x) for _ in range(4) for x in input() if x != '.']
    print(sleight_hands(panel, max_keys))
